    private void isiGrid() {
        delTrid(tblDivisi);
        DefaultTableModel defGrid = (DefaultTableModel) tblDivisi.getModel();
        RsKoprasi = koneksiDB.getRS("select * from dt_divisi");
        try {
            while (RsKoprasi.next()) {
                String[] data = {RsKoprasi.getString("kode"),RsKoprasi.getString("nama")};
                defGrid.addRow(data);
            }
        } catch (SQLException ex) {
            Logger.getLogger(vDivisi.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void delTrid(JTable gg) {
        DefaultTableModel defGrid = (DefaultTableModel) gg.getModel();
        int brs = gg.getRowCount();
        for(int i=0;i<brs;i++) {
            defGrid.removeRow(0);
        }
    }

