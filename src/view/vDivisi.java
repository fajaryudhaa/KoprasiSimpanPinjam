    protected void datatable(){
        Object[] Baris ={"Kode Divisi","Nama Divisi"}; 
        tabmode = new DefaultTableModel(null, Baris);
        try {
                String sql = "SELECT * FROM dt_divisi order by kode asc"; 
                RsKoprasi = koneksiDB.getRS(sql);
                  while (RsKoprasi.next()){
                        tabmode.addRow(new Object[]{
                        RsKoprasi.getString(1),
                        RsKoprasi.getString(2)
                        });
 
                        }
                        tblDivisi.setModel(tabmode);
                } catch (Exception e){
                        
                }
    
    }
    
    private void auto()
    {
       try {
            String sql="select * from  dt_divisi order by kode desc";
            RsKoprasi = koneksiDB.getRS(sql); 
            if (RsKoprasi.next()) {
                String No = RsKoprasi.getString("kode").substring(5);
                String AN = "" + (Integer.parseInt(No) + 1);
                String Nol = "";

                if(AN.length()==1)
                {Nol = "000";}
                else if(AN.length()==2)
                {Nol = "00";}
                else if(AN.length()==3)
                {Nol = "0";}
                else if(AN.length()==4)
                {Nol = "";}

               tKd_divisi.setText("KODE-" + Nol + AN);
            } else {
               tKd_divisi.setText("KODE-0001");
            }

           }catch(Exception ex){
           JOptionPane.showMessageDialog(null, ex);
           }
    }
    
